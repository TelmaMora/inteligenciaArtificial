print("El resultado de la primera derivada es: ")
print(sp.diff(y,x))
f=sp.diff(y,x,x)
print("El resultado de la segunda derivada es: ")
print(f)
rango = np.linspace(-10, 10, 41)
evalua=[]
res=[]
for i in rango:
    values = {x:i}
    res.append(f.evalf(subs=values))
    evalua.append(evaluar(i))
for j in res:
    if(j==0):
        concava="sin conclusion"
        break
    else:
        if(j>0):
            concava="hacia abajo"
        else:
            concava="hacia abajo"
